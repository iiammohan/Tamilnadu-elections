function buildDistrictChart(districts, ta) {
  // Calculate voters per district
  const distData = districts.map(d => ({
    name: ta && DISTRICT_TAMIL[d] ? DISTRICT_TAMIL[d] : d,
    voters: CONSTITUENCIES.filter(c => c.d === d).reduce((s, c) => s + c.t, 0),
    count: CONSTITUENCIES.filter(c => c.d === d).length
  })).sort((a, b) => b.voters - a.voters);

  const textColor = getChartTextColor();
  const gridColor = getChartGridColor();

  if (chartInstances.district) {
    chartInstances.district.destroy();
  }

  chartInstances.district = new Chart(document.getElementById('districtChart'), {
    type: 'bar',
    data: {
      labels: distData.map(d => d.name),
      datasets: [{
        data: distData.map(d => d.voters),
        backgroundColor: distData.map((_, i) => {
          const hue = (i / distData.length) * 240;
          return `hsl(${hue}, 55%, 50%)`;
        }),
        borderRadius: 3,
        maxBarThickness: 28
      }]
    },
    options: {
      indexAxis: 'y',
      responsive: true,
      maintainAspectRatio: false,
      plugins: {
        legend: { display: false },
        tooltip: {
          callbacks: {
            label: (ctx) => {
              const d = distData[ctx.dataIndex];
              const vLabel = currentLang === 'ta' ? 'வாக்காளர்கள்' : 'voters';
              const cLabel = currentLang === 'ta' ? 'தொகுதிகள்' : 'constituencies';
              return `${formatNum(d.voters)} ${vLabel} · ${d.count} ${cLabel}`;
            }
          }
        }
      },
      scales: {
        x: {
          beginAtZero: true,
          grid: { color: gridColor },
          ticks: { color: textColor, callback: v => v >= 1000000 ? (v/1000000).toFixed(1)+'M' : v >= 1000 ? (v/1000)+'K' : v }
        },
        y: {
          grid: { display: false },
          ticks: { color: textColor, font: { size: 10 } }
        }
      }
    }
  });
}

