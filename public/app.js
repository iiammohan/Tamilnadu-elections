const _PARTY_PRIORITY = ['DMK','AIADMK','BJP','INC','TVK','NTK','PMK','VCK','CPI(M)','CPI','DMDK','MDMK'];
const _PARTY_NAME_ALIASES = {
  DMK: ['dravida munnetra kazhagam'],
  AIADMK: ['all india anna dravida munnetra kazhagam', 'all india anna dravida munnetra kalagam'],
  BJP: ['bharatiya janata party'],
  INC: ['indian national congress'],
  PMK: ['pattali makkal katchi'],
  VCK: ['viduthalai chiruthaigal katchi'],
  'CPI(M)': ['communist party of india (marxist)', 'communist party of india(marxist)'],
  CPI: ['communist party of india'],
  DMDK: ['desiya murpokku dravida kazhagam'],
  MDMK: ['marumalarchi dravida munnetra kazhagam'],
  IUML: ['indian union muslim league'],
  AMMK: ['amma makkal munnetra kazagam', 'amma makkal munnettra kazagam'],
  'TMC(M)': ['tamil maanila congress (moopanar)', 'tamil maanila congress(moopanar)'],
  NTK: ['naam tamilar katchi', 'naam tamizhar katchi'],
  KMDK: ['kongunadu makkal desiya katchi'],
  TVK: ['tamilaga vetri kazhagam', 'tamilaga vettri kazhagam'],
};

function _normPartyName(s) {
  return String(s || '').trim().toLowerCase().replace(/\s+/g, ' ');
}

function _candidateMatchesParty(cand, partyCode) {
  if (!cand || !partyCode) return false;
  if (cand.party_code === partyCode) return true;
  const aliases = _PARTY_NAME_ALIASES[partyCode];
  if (!aliases || !cand.party_name) return false;
  return aliases.includes(_normPartyName(cand.party_name));
}

function _candidateCompleteness(cand) {
  if (!cand) return 0;
  let score = 0;
  if (cand.affidavit_pdf_url) score += 4;
  if (cand.photo_url) score += 2;
  if (cand.age != null) score += 1;
  return score;
}

function _candidateIsAccepted(cand) {
  const status = String((cand && cand.nomination_status) || '').trim().toLowerCase();
  if (!status) return true;
  return status === 'accepted' || status.includes('accepted');
}

function _pickBestCandidateByParty(candidates, acNo, partyCode, fallbackName) {
  const matches = candidates.filter(c => _candidateMatchesParty(c, partyCode));
  if (!matches.length) return null;

  const acceptedMatches = matches.filter(_candidateIsAccepted);
  if (!acceptedMatches.length) return null;

  const official = fallbackName || _getOfficialName(acNo, partyCode);
  return acceptedMatches.slice().sort((a, b) => {
    const aScore = _nameScore(official, a.name);
    const bScore = _nameScore(official, b.name);
    if (aScore !== bScore) return bScore - aScore;
    const aComplete = _candidateCompleteness(a);
    const bComplete = _candidateCompleteness(b);
    if (aComplete !== bComplete) return bComplete - aComplete;
    return a.name.localeCompare(b.name);
  })[0];
}

function _pickAllianceParty(candidates, preferredCode, partySet) {
  const eligible = candidates.filter(_candidateIsAccepted);
  if (preferredCode && eligible.some(c => _candidateMatchesParty(c, preferredCode))) {
    return preferredCode;
  }

  const derived = new Set();
  eligible.forEach(cand => {
    if (partySet.has(cand.party_code)) {
      derived.add(cand.party_code);
      return;
    }
    Object.keys(_PARTY_NAME_ALIASES).forEach(code => {
      if (partySet.has(code) && _candidateMatchesParty(cand, code)) derived.add(code);
    });
  });

  const ordered = [...derived].sort((a, b) => {
    const ai = _PARTY_PRIORITY.indexOf(a);
    const bi = _PARTY_PRIORITY.indexOf(b);
    const av = ai >= 0 ? ai : 999;
    const bv = bi >= 0 ? bi : 999;
    if (av !== bv) return av - bv;
    return a.localeCompare(b);
  });

  return ordered[0] || preferredCode || null;
}

function _getVerifiedSlate(constData) {
  const fallback = {
    sp: constData.sp,
    sc: constData.sc,
    np: constData.np,
    nc: constData.nc,
    tc: constData.tc,
    nkc: constData.nkc,
  };

  if (typeof CandidateData === 'undefined' || !CandidateData.isAvailable()) return fallback;

  const rawCandidates = CandidateData.getByAC(constData.ac);
  if (!rawCandidates.length) return fallback;

  const candidates = _filterDummyCandidates(rawCandidates);
  const spaParty = _pickAllianceParty(candidates, constData.sp, _SPA_PARTIES);
  const ndaParty = _pickAllianceParty(candidates, constData.np, _NDA_PARTIES);
  const spaCandidate = spaParty ? _pickBestCandidateByParty(candidates, constData.ac, spaParty, constData.sc) : null;
  const ndaCandidate = ndaParty ? _pickBestCandidateByParty(candidates, constData.ac, ndaParty, constData.nc) : null;
  const tvkCandidate = _pickBestCandidateByParty(candidates, constData.ac, 'TVK', constData.tc);
  const ntkCandidate = _pickBestCandidateByParty(candidates, constData.ac, 'NTK', constData.nkc);
  const tvkHasAny = candidates.some(c => _candidateMatchesParty(c, 'TVK'));
  const ntkHasAny = candidates.some(c => _candidateMatchesParty(c, 'NTK'));

  return {
    sp: spaParty || fallback.sp,
    sc: spaCandidate ? spaCandidate.name : fallback.sc,
    np: ndaParty || fallback.np,
    nc: ndaCandidate ? ndaCandidate.name : fallback.nc,
    tc: tvkCandidate ? tvkCandidate.name : (tvkHasAny ? '—' : fallback.tc),
    nkc: ntkCandidate ? ntkCandidate.name : (ntkHasAny ? '—' : fallback.nkc),
  };
}

/* Return the official candidate name for a party in a constituency (from data.js) */
function _getOfficialName(acNo, partyCode) {
  const c = getConstData(acNo);
  if (!c) return null;
  if (c.sp === partyCode) return c.sc;
  if (c.np === partyCode) return c.nc;
  if (partyCode === 'TVK') return c.tc;
  if (partyCode === 'NTK') return c.nkc;
  return null;
}

/* Score how well an ECI name matches the official name */
function _nameScore(officialName, eciName) {
  if (!officialName || !eciName) return 0;
  const norm = s => s.toLowerCase().replace(/[.\-,]/g, ' ').replace(/\s+/g, ' ').trim();
  const offWords = norm(officialName).split(' ').filter(w => w.length > 0);
  const eciNorm = norm(eciName);
  const eciWords = eciNorm.split(' ').filter(w => w.length > 0);
  let score = 0;
  for (const w of offWords) {
    if (w.length <= 2) {
      // Initial — check if any ECI word starts with it
      if (eciWords.some(ew => ew.startsWith(w))) score += 1;
    } else if (eciNorm.includes(w)) {
      score += 3; // exact word match
    } else {
      // Prefix match — handles Rajasekhar/Rajasekaran, Sukumar/Sukumaran etc.
      const prefix = w.substring(0, Math.max(5, Math.floor(w.length * 0.7)));
      if (eciWords.some(ew => ew.startsWith(prefix))) score += 2;
    }
  }
  return score;
}

/* Remove dummy/substitute candidates — keep 1 per party per AC for recognized parties */
function _filterDummyCandidates(candidates) {
  const groups = {};
  const result = [];
  for (const c of candidates) {
    if (c.party_code === 'IND' || c.party_code === 'Others') {
      result.push(c);
      continue;
    }
    const key = c.ac_no + '_' + c.party_code;
    if (!groups[key]) groups[key] = [];
    groups[key].push(c);
  }
  for (const group of Object.values(groups)) {
    if (group.length === 1) { result.push(group[0]); continue; }
    const acceptedGroup = group.filter(_candidateIsAccepted);
    const pool = acceptedGroup.length ? acceptedGroup : group;
    const official = _getOfficialName(group[0].ac_no, group[0].party_code);
    if (official) {
      // Pick candidate with highest name match score
      let best = pool[0], bestScore = 0;
      for (const c of pool) {
        const s = _nameScore(official, c.name);
        if (s > bestScore) { bestScore = s; best = c; }
      }
      result.push(best);
    } else {
      result.push(pool[0]);
    }
  }
  return result;
}

function _sortCandidates(candidates, constData) {
  const spaParty = constData && constData.sp ? constData.sp : null;
  const ndaParty = constData && constData.np ? constData.np : null;
  return [...candidates].sort((a, b) => {
    function pri(code) {
      if (spaParty && code === spaParty) return 0;
      if (ndaParty && code === ndaParty) return 1;
      if (code === 'TVK') return 2;
      if (code === 'NTK') return 3;
      if (code === 'IND') return 999;
      const i = _PARTY_PRIORITY.indexOf(code);
      return i >= 0 ? 10 + i : 100;
    }
    const ai = pri(a.party_code), bi = pri(b.party_code);
    if (ai !== bi) return ai - bi;
    if (a.party_code !== b.party_code) return a.party_code.localeCompare(b.party_code);
    return a.name.localeCompare(b.name);
  });
}

