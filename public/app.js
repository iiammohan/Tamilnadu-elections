function filterCandidatesTable(query) {
  candCurrentPage = 1;
  const base = candSelectedAC ? candTableData.filter(c => c.ac_no === candSelectedAC) : candTableData;
  if (!query) { candFilteredData = base; renderCandidatesTable(); return; }
  const q = normalizeSearchQuery(query);
  candFilteredData = base.filter(c =>
    c.name.toLowerCase().includes(q) ||
    c.party_code.toLowerCase().includes(q) ||
    c.party_name.toLowerCase().includes(q) ||
    c.ac_name.toLowerCase().includes(q) ||
    (c.district && c.district.toLowerCase().includes(q)) ||
    String(c.ac_no).includes(q) ||
    (CONST_TAMIL[c.ac_name] && CONST_TAMIL[c.ac_name].includes(q)) ||
    (c.district && DISTRICT_TAMIL[c.district] && DISTRICT_TAMIL[c.district].includes(q))
  );
  renderCandidatesTable();
}

function sortCandidatesTable(col) {
  const keys = ['ac_no', 'ac_name', 'district', 'name', 'party_code', 'age', 'gender', 'assets_total', 'liabilities_total', 'pending_cases_count'];
  const key = keys[col];
  if (candSortCol === col) candSortAsc = !candSortAsc;
  else { candSortCol = col; candSortAsc = true; }
  candFilteredData.sort((a, b) => {
    let va = a[key], vb = b[key];
    if (va == null) va = candSortAsc ? Infinity : -Infinity;
    if (vb == null) vb = candSortAsc ? Infinity : -Infinity;
    if (typeof va === 'number' && typeof vb === 'number') return candSortAsc ? va - vb : vb - va;
    va = String(va).toLowerCase(); vb = String(vb).toLowerCase();
    return candSortAsc ? va.localeCompare(vb) : vb.localeCompare(va);
  });
  candCurrentPage = 1;
  renderCandidatesTable();
}

