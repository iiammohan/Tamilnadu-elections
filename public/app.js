function updateCountdown() {
  const target = new Date('2026-05-04T08:00:00+05:30');
  const now = new Date();
  const diff = target - now;
  const el = document.getElementById('resultsCountdown');
  if (!el) return;
  if (diff <= 0) {
    el.textContent = currentLang === 'ta' ? '\u0b8e\u0ba3\u0bcd\u0ba3\u0bbf\u0b95\u0bcd\u0b95\u0bc8 \u0ba8\u0b9f\u0ba8\u0bcd\u0ba4\u0bc1 \u0b95\u0bca\u0ba3\u0bcd\u0b9f\u0bbf\u0bb0\u0bc1\u0b95\u0bcd\u0b95\u0bbf\u0bb1\u0ba4\u0bc1!' : 'Counting is underway!';
    return;
  }
  const days = Math.floor(diff / 86400000);
  const hrs = Math.floor((diff % 86400000) / 3600000);
  const mins = Math.floor((diff % 3600000) / 60000);
  el.textContent = currentLang === 'ta'
    ? `${days} \u0ba8\u0bbe\u0b9f\u0bcd\u0b95\u0bb3\u0bcd ${hrs} \u0bae\u0ba3\u0bbf ${mins} \u0ba8\u0bbf\u0bae\u0bbf\u0b9f\u0bae\u0bcd`
    : `${days} days ${hrs} hours ${mins} minutes`;
}

