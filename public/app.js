function setLanguage(lang) {
  currentLang = normalizeLanguage(lang);
  localStorage.setItem('tneLang', currentLang);
  document.querySelectorAll('.lang-btn').forEach(b => b.classList.toggle('active', b.dataset.lang === lang));
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (lang === 'ta' && TAMIL[key]) {
      if (!el.dataset.en) el.dataset.en = el.textContent;
      el.textContent = TAMIL[key];
    } else if (el.dataset.en) {
      el.textContent = el.dataset.en;
    }
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.dataset.i18nPh;
    if (lang === 'ta' && TAMIL[key]) {
      if (!el.dataset.enPh) el.dataset.enPh = el.placeholder;
      el.placeholder = TAMIL[key];
    } else if (el.dataset.enPh) {
      el.placeholder = el.dataset.enPh;
    }
  });
  // Rebuild dynamic overview content
  if (overviewExtrasBuilt) { overviewExtrasBuilt = false; buildOverviewExtras(); }
  // Repopulate district selector with correct language
  populateDistrictSelector();
  // Rebuild map legend with correct party labels
  if (mapG) updateMapColoring();
  // Rebuild parties if rendered
  if (partiesRendered) { renderPartyCards(); renderAlliances(); }
  // Rebuild table if built
  if (tableBuilt) renderTable(tableData);
  // Re-render charts with correct labels
  if (chartsBuilt) rebuildGenderChart();
  // Translate manifesto static content
  translateManifestoContent();
  // Refresh manifesto audit labels only after the audit section has been rendered once.
  const auditYearEl = document.getElementById('manifestoAuditYear');
  const isAuditActive = document.getElementById('manifesto-audit')?.classList.contains('active');
  if (auditYearEl && (manifestoAuditRendered || isAuditActive)) {
    ensureManifestoAuditRendered(auditYearEl.value || '2021');
  }
  // Rebuild results tab if rendered
  if (resultsBuilt) rebuildResults();
  // Rebuild candidates table if built
  if (candidatesTableBuilt) renderCandidatesTable();
  // Update countdown text
  updateCountdown();
}

