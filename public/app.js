    r.some(cell => String(cell).toLowerCase().includes(q)) ||
    (CONST_TAMIL[r[1]] && CONST_TAMIL[r[1]].includes(q)) ||
    (DISTRICT_TAMIL[r[2]] && DISTRICT_TAMIL[r[2]].includes(q))
