function buildResultsYear(year, partyField, nameField) {
  const ta = currentLang === 'ta';
  const seats = {};
  const districtSeats = {};
  CONSTITUENCIES.forEach(c => {
    const p = c[partyField] || 'Others';
    seats[p] = (seats[p] || 0) + 1;
    if (!districtSeats[c.d]) districtSeats[c.d] = {};
    districtSeats[c.d][p] = (districtSeats[c.d][p] || 0) + 1;
  });

  // Sort parties by seats descending
  const sorted = Object.entries(seats).sort((a, b) => b[1] - a[1]);
  const seatsWord = ta ? 'இடங்கள்' : 'seats';

  // Summary cards
  const summaryEl = document.getElementById(`results${year}Summary`);
  summaryEl.innerHTML = sorted.map(([party, count]) => `
    <div class="results-summary-card" style="border-left:4px solid ${getPartyColor(party)}">
      <div class="rsc-party">${partyLabel(party)}</div>
      <div class="rsc-seats">${count}</div>
      <div class="rsc-label">${seatsWord}</div>
    </div>
  `).join('');

  // Seats bar chart
  const seatsKey = `results${year}Seats`;
  if (chartInstances[seatsKey]) chartInstances[seatsKey].destroy();
  const seatsCtx = document.getElementById(`results${year}SeatsChart`).getContext('2d');
  chartInstances[seatsKey] = new Chart(seatsCtx, {
    type: 'bar',
    data: {
      labels: sorted.map(s => partyLabel(s[0])),
      datasets: [{
        label: ta ? `${year} வென்ற இடங்கள்` : `${year} Seats Won`,
        data: sorted.map(s => s[1]),
        backgroundColor: sorted.map(s => getPartyColor(s[0]))
      }]
    },
    options: {
      responsive: true,
      maintainAspectRatio: false,
      plugins: { legend: { display: false }, title: { display: true, text: ta ? `கட்சி வாரி வென்ற இடங்கள் (${year})` : `Party-wise Seats Won (${year})` } },
      scales: { y: { beginAtZero: true, ticks: { stepSize: 10 } } }
    }
  });

  // District chart — top 12 districts by total seats, stacked by party
  const distSorted = Object.entries(districtSeats).sort((a, b) => {
    const totalA = Object.values(a[1]).reduce((s, v) => s + v, 0);
    const totalB = Object.values(b[1]).reduce((s, v) => s + v, 0);
    return totalB - totalA;
  }).slice(0, 12);
  const allParties = sorted.map(s => s[0]);
  const distKey = `results${year}District`;
  if (chartInstances[distKey]) chartInstances[distKey].destroy();
  const distCtx = document.getElementById(`results${year}DistrictChart`).getContext('2d');
  chartInstances[distKey] = new Chart(distCtx, {
    type: 'bar',
    data: {
      labels: distSorted.map(d => ta && DISTRICT_TAMIL[d[0]] ? DISTRICT_TAMIL[d[0]] : d[0].charAt(0) + d[0].slice(1).toLowerCase()),
      datasets: allParties.filter(p => {
        return distSorted.some(d => d[1][p]);
      }).map(p => ({
        label: partyLabel(p),
        data: distSorted.map(d => d[1][p] || 0),
        backgroundColor: getPartyColor(p)
      }))
    },
    options: {
      responsive: true,
      maintainAspectRatio: false,
      indexAxis: 'y',
      plugins: { title: { display: true, text: ta ? `மாவட்ட வாரி இடங்கள் (முதல் 12) — ${year}` : `District-wise Seats (Top 12) — ${year}` }, legend: { position: 'bottom' } },
      scales: { x: { stacked: true, beginAtZero: true }, y: { stacked: true } }
    }
  });

  // Insights
  buildResultsInsights(year, partyField, nameField, sorted);

  // Winners table
  buildResultsTable(year, partyField, nameField);
}

function buildResultsInsights(year, partyField, nameField, sorted) {
  const el = document.getElementById(`results${year}Insights`);
  const ta = currentLang === 'ta';
  const insights = [];
  const winner = sorted[0];
  if (ta) {
    insights.push(`<strong>${partyLabel(winner[0])}</strong> 234 தொகுதிகளில் <strong>${winner[1]}</strong> இடங்களை வென்று முதலிடம் பிடித்தது.`);
  } else {
    insights.push(`<strong>${winner[0]}</strong> won the most seats with <strong>${winner[1]}</strong> out of 234 constituencies.`);
  }

  if (sorted.length > 1) {
    if (ta) {
      insights.push(`<strong>${partyLabel(sorted[1][0])}</strong> <strong>${sorted[1][1]}</strong> இடங்களுடன் இரண்டாம் இடம் பிடித்தது.`);
    } else {
      insights.push(`<strong>${sorted[1][0]}</strong> came second with <strong>${sorted[1][1]}</strong> seats.`);
    }
  }

  // SPA vs NDA
  let spaSeats = 0, ndaSeats = 0, otherSeats = 0;
  sorted.forEach(([p, c]) => {
    if (_SPA_PARTIES.has(p)) spaSeats += c;
    else if (_NDA_PARTIES.has(p)) ndaSeats += c;
    else otherSeats += c;
  });
  if (ta) {
    insights.push(`<strong>சே.மு.கூ கூட்டணி</strong> <strong>${spaSeats}</strong> இடங்களையும், <strong>தே.ஜ.கூ கூட்டணி</strong> <strong>${ndaSeats}</strong> இடங்களையும், பிற <strong>${otherSeats}</strong> இடங்களையும் வென்றன.`);
  } else {
    insights.push(`<strong>SPA alliance</strong> won <strong>${spaSeats}</strong> seats, <strong>NDA alliance</strong> won <strong>${ndaSeats}</strong> seats, and others won <strong>${otherSeats}</strong>.`);
  }

  // Swing analysis (only for 2021)
  if (year === '2021') {
    const swing = {};
    CONSTITUENCIES.forEach(c => {
      const p21 = c.w21 || 'Others';
      const p16 = c.w16 || 'Others';
      if (p21 !== p16) {
        const key = `${partyLabel(p16)} → ${partyLabel(p21)}`;
        swing[key] = (swing[key] || 0) + 1;
      }
    });
    const swingSorted = Object.entries(swing).sort((a, b) => b[1] - a[1]).slice(0, 5);
    if (swingSorted.length > 0) {
      const seatsWord = ta ? 'இடங்கள்' : 'seats';
      const prefix = ta ? '<strong>2016-லிருந்து முக்கிய மாற்றங்கள்:</strong> ' : '<strong>Major swings from 2016:</strong> ';
      insights.push(prefix + swingSorted.map(([k, v]) => `${k} (${v} ${seatsWord})`).join(', ') + '.');
    }
    // Repeat winners
    let repeats = 0;
    CONSTITUENCIES.forEach(c => { if (c.w21n && c.w16n && c.w21n === c.w16n) repeats++; });
    if (ta) {
      insights.push(`<strong>${repeats}</strong> தொகுதிகளில் 2016 மற்றும் 2021 இரண்டிலும் ஒரே நபர் வெற்றி பெற்றார்.`);
    } else {
      insights.push(`<strong>${repeats}</strong> constituencies had the same winner in both 2016 and 2021.`);
    }
  }

  const heading = ta ? 'முக்கிய முடிவுகள்' : 'Key Insights';
  el.innerHTML = `<h3>${heading}</h3><ul>${insights.map(i => `<li>${i}</li>`).join('')}</ul>`;
}

function buildResultsTable(year, partyField, nameField) {
  const ta = currentLang === 'ta';
  const tbody = document.querySelector(`#results${year}Table tbody`);
  const rows = CONSTITUENCIES.map(c => {
    const party = c[partyField] || '—';
    const name = c[nameField] || '—';
    const cname = ta && CONST_TAMIL[c.n] ? CONST_TAMIL[c.n] : c.n;
    const dist = ta && DISTRICT_TAMIL[c.d] ? DISTRICT_TAMIL[c.d] : c.d;
    return `<tr>
      <td>${c.ac}</td>
      <td>${cname}</td>
      <td>${dist}</td>
      <td>${name}</td>
      <td><span class="party-badge" style="background:${getPartyColor(party)};color:#fff;padding:2px 8px;border-radius:10px;font-size:0.8em">${partyLabel(party)}</span></td>
    </tr>`;
  });
  tbody.innerHTML = rows.join('');
}

function filterResultsTable(year) {
  const query = normalizeSearchQuery(document.getElementById(`results${year}Search`).value);
  const rows = document.querySelectorAll(`#results${year}Table tbody tr`);
  rows.forEach(row => {
    const text = row.textContent.toLowerCase();
    row.style.display = text.includes(query) ? '' : 'none';
  });
}

